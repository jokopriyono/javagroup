package com.javagroup.basic.operator;

public class OperatorAritmatika {
    public static void main(String [] args){

        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 5 + 5;
        System.out.println("Hasil dari 5 + 5 = " + hasilPenjumlahan);
        System.out.println();

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 12 - 7;
        System.out.println("Hasil dari 12 - 7 = " + hasilPengurangan);
        System.out.println();

        System.out.println("Operasi Perkalian");
        int hasilPerkalian = 4 * 5;
        System.out.println("Hasil dari 4 * 5 = " + hasilPerkalian);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasilPembagian = 20 / 5;
        System.out.println("Hasil dari 20 / 5 = " + hasilPembagian);
        System.out.println();

        System.out.println("Operasi Habis Bagi");
        int hasilSisa = 8 % 2;
        System.out.println("Hasil dari 8 % 2 = " + hasilSisa);
        System.out.println();
        int hasilSisa2 = 9 % 2;
        System.out.println("Hasil dari 9 % 2 = " + hasilSisa2);
        System.out.println();

    }
}
